package description;
/**
 * <p>Un TypeAlea définit l'impact qu'a l'apparition d'un alea non contré sur l'environnement d'un joueur.</p>
 * 
 * @author bascopa
 *
 */
public enum TypeAlea {
	/**
	 * Impact sur le temps de réalisation d'une tâche.
	 */
	DELAI,
	/**
	 * Impact sur le cout.
	 */
	COUT,
	/**
	 * Impact sur la qualité du produit fini.
	 */
	QUAL;


}


	
	

